export function initializeSeedData(
  users: User[],
  threads: Thread[],
  comments: Comment[]
): { users: User[]; threads: Thread[]; comments: Comment[] } {
  if (users.length === 0 && threads.length === 0) {
    return {
      users: SEED_USERS,
      threads: SEED_THREADS,
      comments: SEED_COMMENTS,
    }
  }
  
  return { users, threads, comments }
}
